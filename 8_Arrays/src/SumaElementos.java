
public class SumaElementos {

    public static void main(String[] args) {
        //Creando array con 3 elementos tipo entero
        int[] arreglo = new int[3];
        arreglo[0] = 1;
        arreglo[1] = 4;
        arreglo[2] = 6;
        // sumar los 3 elementos
        int sumaArreglo = arreglo[0] + arreglo[1] + arreglo[2];

        System.out.println("La suma de los valores que conforma el array es: " + sumaArreglo);

    }
}
